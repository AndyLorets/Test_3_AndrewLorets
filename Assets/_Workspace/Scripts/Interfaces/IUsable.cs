/// <summary>
/// Интерфейс для любого объекта, который можно "использовать".
/// </summary>
public interface IUsable
{
    /// <summary>
    /// Выполняет действие, связанное с использованием предмета.
    /// </summary>
    /// <param name="target">Опциональная цель, на которую применяется предмет (например, игрок).</param>
    void Use();
}